//1st image and 1st view button
$(document).ready(function(){
    $("#button1").hide();
    $("#img1").hover(function(){
        $("#button1").show();
        $("#button1").hover(function(){
            $("#button1").show();
            $("#img1").css({
                'opacity' : '0.7',
            })
        }, function(){
            $("#button1").hide();
            $("#img1").css({
                'opacity':'10',
            })
        })
    }, function(){
        $("#button1").hide();
    });
});
